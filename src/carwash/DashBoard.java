    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        String name = tfName.getText();
        String emailInquery = tfEmail2.getText();
        String message_title = tfMessageTitle.getText();
        String message_content = taMessageContent.getText();

        if (name.isEmpty() || emailInquery.isEmpty() || message_title.isEmpty() || message_content.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all fields.", "Try again", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Statement stmt = DbConnection.myconn().createStatement();
            String query = "SELECT COUNT(*) FROM inquiries WHERE name='" + name + "' AND email='" + emailInquery + "' AND message_title='" + message_title + "' AND message_content='" + message_content + "'";
            ResultSet rs = stmt.executeQuery(query);
            rs.next();
            int rowCount = rs.getInt(1);

            if (rowCount == 0) {
                query = "INSERT INTO inquiries (name, email, message_title, message_content) VALUES ('" + name + "', '" + emailInquery + "', '" + message_title + "', '" + message_content + "')";
                int affectedRows = stmt.executeUpdate(query, Statement.RETURN_GENERATED_KEYS);

                if (affectedRows == 1) {
                    ResultSet generatedKeys = stmt.getGeneratedKeys();
                    if (generatedKeys.next()) {
                        int inquiryId = generatedKeys.getInt(1);
                        JOptionPane.showMessageDialog(this, "Your message was sent successfully. Your inquiry ID is " + inquiryId );
                        jlInquiryNumber.setText(Integer.toString(inquiryId)); // Update the jlInquiryNumber label with the inquiry ID
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Your inquiry has already been submitted. Please check your email for the inquiry number.");
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_btnSendActionPerformed

